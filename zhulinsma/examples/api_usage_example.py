def 示例_WebAPI调用():
    """示例6：模拟Web API调用"""
    print("\n" + "=" * 60)
    print("示例6：模拟Web API调用")
    print("=" * 60)
    
    from zhulinsma.interface.web_api import WebAPI
    
    # 创建WebAPI实例
    web_api = WebAPI(验证模式=True)
    
    # 健康检查
    print("1. 健康检查:")
    健康响应 = web_api.处理请求("/api/health", "GET")
    if 健康响应.成功:
        print(f"   ✅ 系统健康: {健康响应.数据.get('状态', '未知')}")
    else:
        print(f"   ❌ 健康检查失败: {健康响应.消息}")
    
    # 技术分析请求
    print("\n2. 技术分析请求:")
    价格数据 = [10.5, 10.8, 11.2, 10.9, 11.5, 11.8, 12.1, 11.9, 12.3, 12.5]
    分析请求 = {
        "股票代码": "000001.SZ",
        "价格数据": 价格数据,
        "时间戳": [f"2026-03-{str(i+1).zfill(2)}" for i in range(10)]
    }
    分析响应 = web_api.处理请求("/api/analyze", "POST", 分析请求)
    if 分析响应.成功:
        print(f"   ✅ 分析成功 - 状态码: {分析响应.状态码}")
        print(f"   📊 趋势方向: {分析响应.数据.get('趋势分析', {}).get('趋势方向', '未知')}")
    else:
        print(f"   ❌ 分析失败: {分析响应.消息}")
    
    # 计算SMA指标
    print("\n3. 计算SMA指标:")
    sma请求 = {
        "价格数据": 价格数据,
        "周期": 20
    }
    sma响应 = web_api.处理请求("/api/indicators/sma", "POST", sma请求)
    if sma响应.成功:
        print(f"   ✅ SMA计算成功 - 值: {sma响应.数据.get('值', 'N/A')}")
    else:
        print(f"   ❌ SMA计算失败: {sma响应.消息}")
    
    return [健康响应, 分析响应, sma响应]

