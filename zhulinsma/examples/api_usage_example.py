def 示例_单只股票分析():
    """示例1：分析单只股票"""
    print("=" * 60)
    print("示例1：分析单只股票")
    print("=" * 60)
    
    # 创建API实例
    api = 竹林司马API(验证模式=True)
    
    # 模拟股票数据
    股票代码 = "000001.SZ"
    价格数据 = [10.5, 10.8, 11.2, 10.9, 11.5, 11.8, 12.1, 11.9, 12.3, 12.5, 
               12.8, 13.1, 12.9, 13.2, 13.5, 13.8, 14.1, 13.9, 14.3, 14.5]
    时间戳 = [f"2026-03-{str(i+1).zfill(2)}" for i in range(20)]
    
    # 执行分析
    结果 = api.分析(股票代码, 价格数据, 时间戳)
    
    # 显示结果
    if 结果["成功"]:
        print(f"✅ 分析成功 - 股票: {结果['股票代码']}")
        print(f"   数据质量评分: {结果['数据质量']['评分']}")
        print(f"   趋势方向: {结果['趋势分析']['趋势方向']}")
        print(f"   风险等级: {结果['风险评估']['风险等级']}")
        print(f"   投资建议: {结果['投资建议']['操作方向']}")
        
        # 显示技术指标
        print(f"\n📊 技术指标:")
        for 指标名称, 指标数据 in 结果["技术指标"].items():
            if 指标数据:
                print(f"   {指标名称}: {指标数据.get('值', 'N/A')} ({指标数据.get('状态', '未知')})")
    else:
        print(f"❌ 分析失败: {结果.get('消息', '未知错误')}")
    
    return 结果

