def 示例_计算技术指标():
    """示例3：计算单个技术指标"""
    print("\n" + "=" * 60)
    print("示例3：计算单个技术指标")
    print("=" * 60)
    
    # 模拟价格数据
    价格数据 = [10.5, 10.8, 11.2, 10.9, 11.5, 11.8, 12.1, 11.9, 12.3, 12.5,
               12.8, 13.1, 12.9, 13.2, 13.5, 13.8, 14.1, 13.9, 14.3, 14.5,
               14.8, 15.1, 14.9, 15.2, 15.5, 15.8, 16.1, 15.9, 16.3, 16.5]
    
    # 计算SMA
    print("1. 计算SMA（简单移动平均线）:")
    sma结果 = 计算指标(价格数据, "SMA", {"周期": 20})
    if sma结果["成功"]:
        print(f"   ✅ SMA(20) = {sma结果['数据'].get('值', 'N/A')}")
    else:
        print(f"   ❌ 计算失败: {sma结果.get('消息', '未知错误')}")
    
    # 计算RSI
    print("\n2. 计算RSI（相对强弱指数）:")
    rsi结果 = 计算指标(价格数据, "RSI", {"周期": 14})
    if rsi结果["成功"]:
        print(f"   ✅ RSI(14) = {rsi结果['数据'].get('值', 'N/A')}")
        rsi值 = rsi结果['数据'].get('值', 0)
        if rsi值 > 70:
            print(f"   ⚠️  RSI({rsi值:.2f}) > 70，超买区域")
        elif rsi值 < 30:
            print(f"   ⚠️  RSI({rsi值:.2f}) < 30，超卖区域")
        else:
            print(f"   ✅ RSI({rsi值:.2f}) 正常区域")
    else:
        print(f"   ❌ 计算失败: {rsi结果.get('消息', '未知错误')}")
    
    # 计算MACD
    print("\n3. 计算MACD（指数平滑异同平均线）:")
    macd结果 = 计算指标(价格数据, "MACD")
    if macd结果["成功"]:
        数据 = macd结果['数据']
        print(f"   ✅ MACD = {数据.get('MACD值', 'N/A')}")
        print(f"   ✅ 信号线 = {数据.get('信号线值', 'N/A')}")
        print(f"   ✅ 柱状图 = {数据.get('柱状图值', 'N/A')}")
        
        # 判断信号
        macd值 = 数据.get('MACD值', 0)
        信号值 = 数据.get('信号线值', 0)
        if macd值 > 信号值:
            print(f"   📈 MACD金叉信号（看多）")
        elif macd值 < 信号值:
            print(f"   📉 MACD死叉信号（看空）")
        else:
            print(f"   ↔️  MACD与信号线持平")
    else:
        print(f"   ❌ 计算失败: {macd结果.get('消息', '未知错误')}")
    
    return [sma结果, rsi结果, macd结果]

def 示例_数据质量验证():
    """示例4：验证数据质量"""
