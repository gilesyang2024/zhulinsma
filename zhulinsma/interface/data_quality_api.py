    def _计算平均质量(self) -> float:
        """计算平均质量分数"""
        if not self.监控数据:
            return 0.0
        
        质量分数列表 = [d.get('质量分数', 0) for d in self.监控_data.values()]
        return float(np.mean(质量分数列表)) if 质量分数列表 else 0.0
    
    def _计算质量分布(self) -> Dict:
        """计算质量分布"""
        分布 = {'优秀(≥0.95)': 0, '良好(0.9-0.95)': 0, '一般(0.8-0.9)': 0, '较差(<0.8)': 0}
        
        for 股票数据 in self.监控_data.values():
            质量分数 = 股票数据.get('质量分数', 0)
            
            if 质量分数 >= 0.95:
                分布['优秀(≥0.95)'] += 1
            elif 质量分数 >= 0.9:
                分布['良好(0.9-0.95)'] += 1
            elif 质量分数 >= 0.8:
                分布['一般(0.8-0.9)'] += 1
            else:
                分布['较差(<0.8)'] += 1
        
        return 分布
    
    def _生成仪表板建议(self) -> List[str]:
        """生成仪表板建议"""
        建议 = []
        
        # 检查平均质量
        平均质量 = self._计算平均质量()
        if 平均质量 < 0.9:
            建议.append(f"平均数据质量较低({平均质量:.3f})，建议关注数据质量问题")
        
