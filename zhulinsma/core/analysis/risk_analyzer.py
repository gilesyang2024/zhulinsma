#!/usr/bin/env python3
"""
竹林司马 (Zhulinsma) - 风险分析器
从波动率、趋势、超买超卖、量价异常等多维度量化投资风险
"""

import numpy as np
from typing import Dict, List, Optional, Union


class RiskAnalyzer:
    """
    风险分析器

    功能：
    - 波动率风险（ATR/标准差/历史波动率）
    - 趋势风险（逆趋势操作惩罚）
    - 超买超卖风险（RSI越界检测）
    - 量价异常风险（成交量与价格背离）
    - 综合风险评分（0~100）与四级评级
    """

    # 风险阈值配置
    _高波动阈值 = 0.04   # 日波动率 > 4% 视为高风险
    _中波动阈值 = 0.02   # 日波动率 > 2% 视为中风险
    _RSI超买 = 70
    _RSI超卖 = 30

    def 评估风险(
        self,
        收盘价: Union[np.ndarray, List[float]],
        成交量: Optional[Union[np.ndarray, List[float]]] = None,
        rsi: Optional[float] = None,
        adx: Optional[float] = None,
    ) -> Dict:
        """
        执行综合风险评估

        参数:
            收盘价: 收盘价序列
            成交量: 成交量序列（可选）
            rsi: 最新 RSI 值（可选，传入时直接使用）
            adx: 最新 ADX 值（可选）

        返回:
            风险评估结果字典
        """
        收盘 = np.asarray(收盘价, dtype=float)

        波动风险 = self._评估波动风险(收盘)
        超买卖风险 = self._评估超买卖风险(rsi)
        量价风险 = self._评估量价风险(收盘, 成交量)
        趋势风险 = self._评估趋势风险(收盘, adx)

        综合分 = self._综合评分(波动风险, 超买卖风险, 量价风险, 趋势风险)
        风险等级 = self._映射等级(综合分)

        return {
            "综合风险分数": round(综合分, 1),
            "风险等级": 风险等级,
            "波动风险": 波动风险,
            "超买卖风险": 超买卖风险,
            "量价风险": 量价风险,
            "趋势风险": 趋势风险,
            "风险因素": self._提取风险因素(波动风险, 超买卖风险, 量价风险, 趋势风险),
            "风险说明": self._生成说明(风险等级),
            "操作建议": self._生成建议(综合分, 风险等级),
        }

    # ──────────────────────────────────────────────
    # 各维度风险评估
    # ──────────────────────────────────────────────

    def _评估波动风险(self, 收盘: np.ndarray, 周期: int = 20) -> Dict:
        """历史波动率 (HV20) 评估"""
        if len(收盘) < 周期 + 1:
            return {"等级": "未知", "分数": 50, "日波动率": None}

        收益率 = np.diff(np.log(收盘[-周期 - 1:]))
        日波动率 = float(np.std(收益率))
        年化波动率 = 日波动率 * np.sqrt(252)

        if 日波动率 > self._高波动阈值:
            等级, 分数 = "高", 75
        elif 日波动率 > self._中波动阈值:
            等级, 分数 = "中", 50
        else:
            等级, 分数 = "低", 25

        return {
            "等级": 等级,
            "分数": 分数,
            "日波动率": round(日波动率, 6),
            "年化波动率": round(年化波动率, 4),
        }

    def _评估超买卖风险(self, rsi: Optional[float]) -> Dict:
        """RSI 超买超卖风险"""
        if rsi is None:
            return {"等级": "未知", "分数": 40, "RSI": None, "状态": "无数据"}

        if rsi >= self._RSI超买:
            等级, 分数, 状态 = "高", 80, "超买区域"
        elif rsi >= 60:
            等级, 分数, 状态 = "中", 55, "偏高区域"
        elif rsi <= self._RSI超卖:
            等级, 分数, 状态 = "高", 70, "超卖区域"
        elif rsi <= 40:
            等级, 分数, 状态 = "中", 45, "偏低区域"
        else:
            等级, 分数, 状态 = "低", 30, "正常区域"

        return {"等级": 等级, "分数": 分数, "RSI": round(rsi, 2), "状态": 状态}

    def _评估量价风险(
        self,
        收盘: np.ndarray,
        成交量: Optional[Union[np.ndarray, List[float]]],
        周期: int = 10,
    ) -> Dict:
        """量价背离风险检测"""
        if 成交量 is None or len(成交量) < 周期 + 1:
            return {"等级": "未知", "分数": 40, "背离状态": "无成交量数据"}

        量 = np.asarray(成交量, dtype=float)
        价格趋势 = 收盘[-1] - 收盘[-周期]
        量趋势 = 量[-1] - np.mean(量[-周期:])

        # 价涨量缩 → 量价背离（看跌）
        if 价格趋势 > 0 and 量趋势 < -np.std(量[-周期:]) * 0.5:
            背离状态, 等级, 分数 = "价涨量缩（看跌背离）", "高", 75
        elif 价格趋势 < 0 and 量趋势 > np.std(量[-周期:]) * 0.5:
            背离状态, 等级, 分数 = "价跌量增（恐慌性抛售）", "高", 70
        elif 价格趋势 > 0 and 量趋势 > 0:
            背离状态, 等级, 分数 = "价涨量增（健康上涨）", "低", 25
        elif 价格趋势 < 0 and 量趋势 < 0:
            背离状态, 等级, 分数 = "价跌量缩（缩量调整）", "低", 30
        else:
            背离状态, 等级, 分数 = "量价同步", "中", 45

        return {"等级": 等级, "分数": 分数, "背离状态": 背离状态}

    def _评估趋势风险(self, 收盘: np.ndarray, adx: Optional[float]) -> Dict:
        """基于趋势强度的风险评估：无趋势环境做方向性操作风险高"""
        if adx is None:
            if len(收盘) < 20:
                return {"等级": "未知", "分数": 50, "ADX": None}
            收益率 = np.diff(收盘[-20:])
            方向一致性 = float(np.sum(np.sign(收益率) == np.sign(收益率[-1])) / len(收益率))
            分数 = int((1 - 方向一致性) * 60 + 20)
            等级 = "低" if 方向一致性 > 0.6 else ("中" if 方向一致性 > 0.4 else "高")
            return {"等级": 等级, "分数": 分数, "ADX": None, "说明": "基于价格一致性估算"}

        if adx > 40:
            等级, 分数 = "低", 20
        elif adx > 25:
            等级, 分数 = "低", 30
        elif adx > 15:
            等级, 分数 = "中", 55
        else:
            等级, 分数 = "高", 75

        return {"等级": 等级, "分数": 分数, "ADX": round(adx, 2)}

    # ──────────────────────────────────────────────
    # 综合评分与映射
    # ──────────────────────────────────────────────

    def _综合评分(self, *风险字典列表) -> float:
        """加权平均各维度分数"""
        权重 = [0.35, 0.25, 0.20, 0.20]
        总分 = 0.0
        for w, d in zip(权重, 风险字典列表):
            总分 += w * d.get("分数", 50)
        return 总分

    def _映射等级(self, 分数: float) -> str:
        if 分数 >= 70:
            return "极高风险"
        elif 分数 >= 55:
            return "高风险"
        elif 分数 >= 40:
            return "中风险"
        else:
            return "低风险"

    def _提取风险因素(self, *风险字典列表) -> List[str]:
        因素 = []
        标签 = ["波动风险", "超买卖风险", "量价风险", "趋势风险"]
        for 标签名, d in zip(标签, 风险字典列表):
            if d.get("等级") in ("高", "极高"):
                因素.append(f"{标签名}({d.get('等级', '')})")
        return 因素 if 因素 else ["无明显高风险因素"]

    def _生成说明(self, 等级: str) -> str:
        return {
            "低风险": "当前市场环境较好，风险可控，适合积极操作。",
            "中风险": "市场存在一定波动，建议控制仓位，谨慎操作。",
            "高风险": "风险明显偏高，建议轻仓或观望，设置严格止损。",
            "极高风险": "市场风险极大，建议空仓等待，切勿重仓追涨。",
        }.get(等级, "风险等级评估中。")

    def _生成建议(self, 分数: float, 等级: str) -> str:
        if 等级 == "低风险":
            return "可适当加仓，控制单次买入不超过可用资金30%。"
        elif 等级 == "中风险":
            return "维持现有仓位，设置5%止损，分批操作。"
        elif 等级 == "高风险":
            return "减仓至轻仓（≤15%），等待风险释放后再入场。"
        else:
            return "建议空仓观望，不参与当前高风险行情。"
